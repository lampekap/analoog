function groot(i){
switch (i){
              case 0:
              case 1:
              case 2:
                     document.body.style.backgroundImage = "url('images/night.png')";
                     break;
              case 3:
              case 4:
              case 5:
                     document.body.style.backgroundImage = "url('images/lateNight.png')";
                     break;
              case 6:
              case 7:
              case 8:
                     document.body.style.backgroundImage = "url('images/morning.png')";
                     break;
              case 9:
              case 10:
              case 11:
                     document.body.style.backgroundImage = "url('images/lateMorning.png')";
                     break;
              case 12:
              case 13:
              case 14:
                     document.body.style.backgroundImage = "url('images/afternoon.png')";
                     break;
              case 15:
              case 16:
              case 17:
                     document.body.style.backgroundImage = "url('images/lateAfternoon.png')";
                     break;
              case 18:
              case 19:
              case 20:
                     document.body.style.backgroundImage = "url('images/evening.png')";
                     break;
              default:
                     document.body.style.backgroundImage = "url('images/lateEvening.png')";                                          
       }
   }

